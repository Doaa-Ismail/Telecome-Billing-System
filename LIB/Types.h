#ifndef TYPES_H_
#define TYPES_H_
/* Type Define Unsigned Char to be  u8                      */
typedef unsigned char u8;
/* Type Define Unsigned Short Integar to be  u16            */
typedef unsigned short int u16;
/* Type Define Unsigned Long Integar to be  u32             */
typedef unsigned long int u32;

/* Type Define Signed Char to be  s8                        */
typedef signed char s8;
/* Type Define Signed Short Integar to be  s16              */
typedef signed short int s16;
/* Type Define Signed Long Integar to be  s32               */
typedef signed long int s32;

/* Type Define Unsigned Long Long Integar to be  u64        */
typedef unsigned long long int u64 ;
/* Type Define Signed Long Long Integar to be  s64          */
typedef signed long long int s64 ;

/* Type Define Float Number to be  f32                      */
typedef float  f32;
/* Type Define double Number to be  f64                     */
typedef double f64;

#endif /* TYPES_H_ */
